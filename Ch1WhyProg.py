# SYNTAX - Like sentence structure in natural languages there are "Syntax" with its valid patterns in Python language.

x = 2  # Assignment Statement, "x" is a Variable, "2" is a Constant
x = x + 2  # Assignment with expression, "=" and "+" are Operators
print(x)  # Print Statement, "print" is a Reserved Word and also a Function


# SCRIPTS - as the paragraphs in natural languages Python has its "Scripts"

# Interactive Python is good for experiments and programs of 3 to 4 lines long
# Most programs are much longer, so we type them into a file and tell Python to run the commands in the file
# In a sentence we are "giving Python a Script"
# As a conversation, we add ".py" as the suffix on the end of these files to indicate they contain Python programs.

# PROGRAM FLOW -
# Like a recipe or installation instruction, a program is a sequence of steps to be done in orders.
# Some steps are conditional - they may be skipped.
# Sometimes a steps or a group of steps is to be repeated.
# Sometimes we store a set of steps to be used over and over as needed several places throughout the program.
# When a program is running, it flows from one step to the next. As programmers, we set up "paths" for program to follow.


x = 2
print(x)
x = x + 2
print(x)


